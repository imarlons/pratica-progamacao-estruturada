# EXERCÍCIO 01

# 1. solicite ao usuário um valor.
# 2. solicite ao usuário outro valor.
# 3. compare os dois valores informados e informe se são iguais ou diferentes.

# entrada
valor1 = int(input('Informe o primeiro número: '))
valor2 = int(input('Informe o segundo número: '))

# processamento
if (valor1 == valor2):
    print('Os valores são iguais!')
else:
    print('Os valores são diferentes!')