# alguns países medem temperaturas em graus Celsius, e outros em graus Fahrenheit
# faça um algoritmo para ler uma temperatura Celsius e imprimi-Ia em Fahrenheit
# fórmula: (°C × 9/5) + 32 = 32 °F

# entrada
celsius = int(input('Informe a temperatura da sua cidade em °C: '))

# processamento 
fahrenheit = (celsius * 9/5) + 32

# saída
print('A temperatura da sua cidade em Fahrenheit é {}°'.format(fahrenheit))





