# faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

# entrada
lado = float(input('Informe o tamanho do lado do seu quadrado: '))

# procesasmento
area = lado * lado
dobro = area * 2

# saída
print('A área do seu quadrado é {} e o seu dobro é {}'.format(area, dobro))

