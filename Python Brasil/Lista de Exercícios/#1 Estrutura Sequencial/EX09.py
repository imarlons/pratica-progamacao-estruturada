# faça um programa que peça a temperatura em graus Fahrenheit, 
# transforme e mostre a temperatura em graus Celsius
# fórmula: (°C × 9/5) + 32 = 32 °F

# entrada
celsius = int(input('Informe a temperatura da sua cidade em °C: '))

# processamento 
fahrenheit = (celsius * 9/5) + 32

# saída
print('A temperatura da sua cidade em Fahrenheit é {}°'.format(fahrenheit))





