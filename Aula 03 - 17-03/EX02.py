# utilizando substring:
# 1. imprima os caracteres nos índices pares.
# 2. imprima os caracteres nos índices ímpares.

nome = 'Ana Maria da Silva'

par = nome[::2]
print('Caracteres nos índices pares: {}'.format(par))
impar = nome[1::2]
print('Caracteres nos índices ímpares: {}'.format(impar))

